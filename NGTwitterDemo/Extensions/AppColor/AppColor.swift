import UIKit.UIColor

extension UIColor {
	
	enum MainTheme {
		static var activeElement: UIColor {
			return #colorLiteral(red: 0.2705882353, green: 0.6, blue: 0.8784313725, alpha: 1)
		}
		
		static var inactiveElement: UIColor {
			return #colorLiteral(red: 0.404329896, green: 0.4478793144, blue: 0.4734104276, alpha: 1)
		}
	}
	
}
